CREATE TABLE book(
    bid     INT PRIMARY KEY AUTO_INCREMENT,
    title   VARCHAR(20) NOT NULL DEFAULT '',
    price   DECIMAL(10,2) NOT NULL DEFAULT 0,
    pubDate DATETIME NOT NULL DEFAULT 0,
    expire  ENUM('1','0') NOT NULL DEFAULT '0'
);
